    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
       int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
       if(getAnswer == JOptionPane.YES_OPTION){
           try{
              pc.deleteDataKendaraan(searchInput.getText());
              clearText();
              showPustaka();
              JOptionPane.showConfirmDialog(null,"Data berhasil dihapus!");
         }catch (Exception e){
               System.out.println("Error : "+e.getMessage());
         }
       }else{
           JOptionPane.showMessageDialog(null, "Data tidak jadi dihapus");
       }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            idException();
            jenisException();
            
            int edisi = 0;
            int volume =0;
            
            Pustaka pus = null;
            
            if(radioBuku.isSelected()==true){
                String jenis = "Buku";
                pus = new Pustaka(idInput.getText(),
                judulInput.getText(),
                jenis,
                tahunInput.getText(),
                penerbitInput.getText(),
                Integer.parseInt(edisiInput.getText()),
                volume);
            }else if(radioMajalah.isSelected()==true){
                String jenis = "Majalah";
                pus = new Pustaka(idInput.getText(),
                judulInput.getText(),
                jenis,
                tahunInput.getText(),
                penerbitInput.getText(),
                edisi,
                Integer.parseInt(volumeInput.getText()));
            }
            
            if(action.equals("Tambah")){
                pc.insertDataPustaka(pus);
            }else{
                pc.updateDataPustaka(pus, idInput.getText());
                JOptionPane.showConfirmDialog(null, "Data Berhasil diubah");
            }
            
            clearText();
            setComponent(false);
            setEditDeleteBtn(false);
            showPustaka();
        }catch(idException e){
            JOptionPane.showConfirmDialog(this, e.message());
        }catch(jenisException e2){
           JOptionPane.showMessageDialog(this, e2.message());
        }catch(NumberFormatException e3){
             JOptionPane.showMessageDialog(this, e3.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed


    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        setComponent(false);
        
        try{
            Pustaka pus = pc.searchDataPustaka(searchInput.getText());
            if(pus == null){
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                idInput.setText(pus.getId_pustaka());
                judulInput.setText(pus.getJudul());
                tahunInput.setText(pus.getTahunTerbit());
                penerbitInput.setText(pus.getPenerbit());
                if(pus.getJenis().equalsIgnoreCase("Buku")){
                    radioBuku.setSelected(true);
                    edisiInput.setText(Integer.toString(pus.getEdisi()));
                }else{
                    radioMajalah.setSelected(true);
                    volumeInput.setText(Integer.toString(pus.getVolume()));
                }
            }
            
        }catch(Exception e) {
            System.out.println("Error : "+e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

